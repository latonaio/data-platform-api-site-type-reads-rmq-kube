package dpfm_api_caller

import (
	"context"
	dpfm_api_input_reader "data-platform-api-site-type-reads-rmq-kube/DPFM_API_Input_Reader"
	dpfm_api_output_formatter "data-platform-api-site-type-reads-rmq-kube/DPFM_API_Output_Formatter"
	"fmt"
	"strings"
	"sync"

	"github.com/latonaio/golang-logging-library-for-data-platform/logger"
)

func (c *DPFMAPICaller) readSqlProcess(
	ctx context.Context,
	mtx *sync.Mutex,
	input *dpfm_api_input_reader.SDC,
	output *dpfm_api_output_formatter.SDC,
	accepter []string,
	errs *[]error,
	log *logger.Logger,
) interface{} {
	var siteType *[]dpfm_api_output_formatter.SiteType
	var text *[]dpfm_api_output_formatter.Text
	for _, fn := range accepter {
		switch fn {
		case "SiteType":
			func() {
				siteType = c.SiteType(mtx, input, output, errs, log)
			}()
		case "SiteTypes":
			func() {
				siteType = c.SiteTypes(mtx, input, output, errs, log)
			}()
		case "Text":
			func() {
				text = c.Text(mtx, input, output, errs, log)
			}()
		case "Texts":
			func() {
				text = c.Texts(mtx, input, output, errs, log)
			}()
		default:
		}
	}

	data := &dpfm_api_output_formatter.Message{
		SiteType: siteType,
		Text:      text,
	}

	return data
}

func (c *DPFMAPICaller) SiteType(
	mtx *sync.Mutex,
	input *dpfm_api_input_reader.SDC,
	output *dpfm_api_output_formatter.SDC,
	errs *[]error,
	log *logger.Logger,
) *[]dpfm_api_output_formatter.SiteType {
	where := fmt.Sprintf("WHERE SiteType = '%s'", input.SiteType.SiteType)

	if input.SiteType.IsMarkedForDeletion != nil {
		where = fmt.Sprintf("%s\nAND IsMarkedForDeletion = %v", where, *input.SiteType.IsMarkedForDeletion)
	}

	rows, err := c.db.Query(
		`SELECT *
		FROM DataPlatformMastersAndTransactionsMysqlKube.data_platform_site_type_site_type_data
		` + where + ` ORDER BY IsMarkedForDeletion ASC, SiteType DESC;`,
	)
	if err != nil {
		*errs = append(*errs, err)
		return nil
	}
	defer rows.Close()

	data, err := dpfm_api_output_formatter.ConvertToSiteType(rows)
	if err != nil {
		*errs = append(*errs, err)
		return nil
	}

	return data
}

